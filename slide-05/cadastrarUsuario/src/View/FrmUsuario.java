    private void btn_cadastrar1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_cadastrar1ActionPerformed
        user.setNome(txt_nome.getText());
        user.setLogin(txt_login.getText());
        user.setEmail(txt_email.getText());
        user.setSenha(txt_senha.getText());
        user.cadastrarUsuario();
        
        ResultSet tabela;
        tabela = null;
        
        tabela = user.consultar();
        DefaultTableModel modelo = (DefaultTableModel) jtbl_usuario.getModel();
        modelo.setNumRows(0);
        
        try{
            do{
                modelo.addRow(new String[]{tabela.getString(1), tabela.getString(2), tabela.getString(3), tabela.getString(4)});
            }
            while(tabela.next());
        }catch(SQLException erro){
            JOptionPane.showMessageDialog(null, "Erro ao preencher a tabela"+erro);
        }
    
    }//GEN-LAST:event_btn_cadastrar1ActionPerformed

